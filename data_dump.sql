CREATE DEFINER=`root`@`localhost` PROCEDURE `purchase`(
	IN bookID_ INT,
	IN login_name_ VARCHAR(15),
	IN amount INT
	
)
BEGIN
	DECLARE book_stock INT DEFAULT 0;
	DECLARE c_balance FLOAT DEFAULT 0.0;
	DECLARE price_ FLOAT DEFAULT 0.0;

	SELECT balance
	INTO c_balance
	FROM customer_data
	WHERE login_name = login_name_;

	SELECT stock
	INTO book_stock
	FROM book_data
	WHERE bookID = bookID_;

	SELECT price
	INTO price_
	FROM book_data
	WHERE bookID = bookID_;



	IF book_stock >= amount AND c_balance >= price_*amount THEN
	
		UPDATE book_data
		SET stock = stock - amount
		WHERE bookID = bookID_;

		UPDATE book_data
		SET total_sold = total_sold + 1
		WHERE bookID = bookID_;

		UPDATE customer_data c, book_data b
		SET c.balance = c.balance - (b.price * amount)
		WHERE c.login_name = login_name_ AND b.bookID = bookID_;

		INSERT INTO transactions(bookID, login_name)
		VALUES(bookID_, login_name_);

		SELECT 'success' AS '';

	ELSE
		SELECT 'fail' AS '';

	END IF;

	
	
END;
